package com.example.pokemonmartin.models;

import java.util.List;

public class CardItem {

    private String id;
    private String name;

    public CardItem(String id, String name) {
        this.id = id;
        this.name = name;
    }

    public String getId() {
        return id;
    }

    public void setId(String id) {
        this.id = id;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }
}
